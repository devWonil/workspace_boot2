package ch06;

//쓰지 않아도 자동 import
import java.lang.*;
// Object 클래스
// 자바에서는 단일 상속만을 지원합니다
// 단, Object는 자바에서 모든 클래스의 최상위 클래스입니다
public class Student extends Object {

	public static void main(String[] args) {
		System.out.println();
		// java.lang.*
		// 기본적으로 많이 사용하는 기본 클래스 묶음
		
		// 게이트웨이 (Object)
	}
}
